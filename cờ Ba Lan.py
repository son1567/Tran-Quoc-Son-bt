import turtle
tl=turtle.Turtle()
tl.speed(1)

tl.forward(180)
tl.right(90)
tl.forward(120)
tl.right(90)
tl.forward(180)
tl.right(90)
tl.forward(120)
tl.right(180)
tl.forward(60)
tl.fillcolor('red')
tl.begin_fill()
tl.left(90)
tl.forward(180)
tl.right(90)
tl.forward(60)
tl.right(90)
tl.forward(180)
tl.right(90)
tl.forward(60)
tl.end_fill()
tl.begin_fill()
turtle.done()


