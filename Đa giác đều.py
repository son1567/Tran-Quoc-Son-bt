import turtle 

tl= turtle.Turtle()
tl.hideturtle()
tl.speed(1)
x=80
y=36

tl.fillcolor('blue')
tl.begin_fill()
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(y)
tl.forward(x)
tl.left(36)
tl.forward(y)
tl.left(x)
tl.forward(y)
tl.end_fill()


turtle.done()

